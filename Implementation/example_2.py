#구현 예제 4-2(완전 탐색 유형)

N = int(input())
result = 0

# 00:00 ~ 00:59  :  15
# 0 ~ 9 : 1
# 10~19 : 1
# 00~29 : 1
# 30~39 : 10
# 40~49 : 1
# 50~59 : 1

# 00:00~09:59  :  15*9 + 60 = 195
# 10:00~19:59  :  15*9 + 60 = 195
# 20:00~29:59  :  15*9 + 60 = 195
# 30:00~39:59  :  60*10 = 600
# 40:00~49:59  :  15*9 + 60 = 195
# 50:00~59:59  :  15*9 + 60 = 195

result += (N // 3) * 3600
result += (195 * 5 + 600) * (N + 1 - N // 3)

print(result)

